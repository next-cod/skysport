# Быстрый доступ по id
_BY_ID = {p["id"]: p for p in PRODUCTS}


# ─── Хелперы ───────────────────────────────────────────────────────
def format_price(n: int) -> str:
    """1490 -> '1 490 ₽'"""
    return f"{n:,.0f} ₽".replace(",", " ")


def get_product(pid: int):
    return _BY_ID.get(pid)


def category_label(key: str) -> str:
    for c in CATEGORIES:
        if c["key"] == key:
            return c["label"]
    return key


def products_by_category(cat: str):
    return [p for p in PRODUCTS if p["category"] == cat]


def featured():
    """Хиты продаж – товары с бейджем."""
    return [p for p in PRODUCTS if p["badge"]]


def search(query: str):
    q = query.strip().lower()
    if not q:
        return []
    return [
        p for p in PRODUCTS
        if q in p["name"].lower() or q in p["categoryLabel"].lower()
    ]


def image_path(p) -> Path | None:
    """Путь к картинке товара или None, если файла нет."""
    path = LANDING_IMAGES / p["img"]
    return path if path.exists() else None


def stars(rating: float) -> str:
    full = round(rating)
    return "★" * full + "☆" * (5 - full)
